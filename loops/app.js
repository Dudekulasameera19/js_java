//for loop
for(let i=0;i<5;i++) {
    console.log('hello world');
}
//while loop
let j=0;
while (j<10){
    console.log(j);
    j++;
}

let k=1;
do{
    console.log(k);
    k++;
}while(k<10);

//do while loop
let arr = [10, 20, 30, 40];
let i = 0;

do {
    console.log(arr[i]);
    i++;
} while (i < arr.length);


